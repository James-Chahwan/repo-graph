# ---------------------------------------------------------------------------
# Git helpers
# ---------------------------------------------------------------------------


def git(repo: Path, cmd: list[str]) -> str:
    try:
        return subprocess.check_output(
            ["git", "-C", str(repo)] + cmd,
            text=True,
            stderr=subprocess.DEVNULL,
        ).strip()
    except Exception:
        return "?"


def git_state_section(repo: Path) -> str:
    """Generate a markdown section describing the repo's git state."""
    branch = git(repo, ["branch", "--show-current"]) or "(detached)"
    sha = git(repo, ["rev-parse", "--short", "HEAD"])

    staged = git(repo, ["diff", "--cached", "--name-only"])
    unstaged = git(repo, ["diff", "--name-only"])
    if staged == "?":
        staged = ""
    if unstaged == "?":
        unstaged = ""
    changed = sorted(set((staged + "\n" + unstaged).strip().splitlines()))
    changes_str = (
        "\n".join(f"  {f}" for f in changed) if changed else "  (none)"
    )

    log_lines = git(repo, ["log", "--oneline", "-5"]).splitlines()
    recent = (
        "\n".join(f"  {l}" for l in log_lines) if log_lines else "  (none)"
    )

    return f"""Branch: {branch} @ {sha}

### Uncommitted changes
{changes_str}

### Recent commits
{recent}"""


