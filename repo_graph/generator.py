def write_graph_outputs(
    graph_dir: Path,
    nodes: list[dict],
    edges: list[dict],
    flows: dict[str, str],
) -> None:
    graph_dir.mkdir(parents=True, exist_ok=True)
    flows_dir = graph_dir / "flows"
    flows_dir.mkdir(exist_ok=True)

    (graph_dir / "nodes.json").write_text(
        json.dumps(nodes, indent=2), encoding="utf-8"
    )
    (graph_dir / "edges.json").write_text(
        json.dumps(edges, indent=2), encoding="utf-8"
    )

    # Clean old flow files
    for old in flows_dir.glob("*.yaml"):
        old.unlink()

    for feat_name, yaml_content in flows.items():
        (flows_dir / f"{feat_name}.yaml").write_text(yaml_content, encoding="utf-8")


def write_state_md(
    graph_dir: Path,
    repo_root: Path,
    state_sections: dict[str, str],
    flows: dict[str, str],
) -> None:
    flows_list = ", ".join(sorted(flows.keys())) if flows else "(none)"

    sections = [
        "# Repo State\n",
        "*Generated by `repo-graph-generate`. Regenerate: `repo-graph-generate --repo .`*\n",
        "---\n",
        "## Git State\n",
        git_state_section(repo_root),
    ]

    if state_sections:
        sections.append("\n---\n")
        for title, content in state_sections.items():
            sections.append(f"## {title}\n")
            sections.append(content)

    sections.append("\n---\n")
    sections.append(f"## Available Flows\n\n{flows_list}\n")

    (graph_dir / "state.md").write_text("\n".join(sections), encoding="utf-8")


