"""
repo-graph generator.

Orchestrates language-specific analyzers to produce:
  - nodes.json   — all discovered entities
  - edges.json   — directed relationships between entities
  - flows/*.yaml — end-to-end request paths per feature
  - state.md     — session-start snapshot

Usage:
    repo-graph-generate --repo /path/to/target-repo
"""

import argparse
